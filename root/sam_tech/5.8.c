#include <stdio.h>
/* Given three integers, 15, 150, and 1500, 
 * write a program that prints the 
 * integers on the screen in the hex format.
 */
int main(void)
{
	int INT1 = 15;
	int INT2 = 150;
	int INT3 = 1500;

	printf("%x\n", INT1);
	printf("%x\n", INT2);
	printf("%x\n", INT3);
	return (0);
}
