#include <stdio.h>
/* Given two numeric values, 72 and 104, write a program to print out the corresponding two characters.*/

int main()
{
	char ch1, ch2;

	ch1 = 72;
	ch2 = 104;

	printf("The corresponding character value of 72 is: %c\n", ch1);
	printf("The corresponding character value of 104 is: %c\n", ch2);
	return (0);
}
